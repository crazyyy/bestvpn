                    <h2>A short guide about VPNs</h2>
                    <p>If you’ve never heard of VPNs, this short paragraph is for you! While the <a title="VPN Wikipedia" href="http://en.wikipedia.org/wiki/Virtual_private_network" target="_blank">Wikipedia article</a> on VPNs does a good job of explaining, it’s somewhat long and technical so we thought we’d try to explain it in simpler terms.</p>
                    <p><b>A VPN is a server somewhere that you connect to, and route your internet usage through it. This not only hides your identity, but also makes it appear as though you are in the country where the server is.</b>
                    </p>
                    <p>About a decade ago (and still to this day) VPNs were servers inside companies that employees could connect to so they could work from home. More and more they are being used as a good way to hide your identity online, which has become especially important in the last few months since the <a href="http://www.bestvpn.com/blog/7245/concern-over-nsas-assault-on-liberty-grows-in-the-us/">NSA snooping revelations</a>. Before, it was only people in China who were trying to get round their “great firewall”, now it’s US citizens, and residents of all countries around the world.</p>
                    <p>While a VPN does cost money, on average it is only $10-$15 per month, and can go as low as $5 (and even less), which is a small price to pay for what they offer. It’s important to check what each service offers, and what their policies are on privacy, as some services will log when and where you logged on from. <a href="http://www.bestvpn.com/">Best VPN</a> tries to help you choose the right service to suit you.</p>
